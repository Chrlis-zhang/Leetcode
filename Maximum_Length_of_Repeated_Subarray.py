        length_B = len(B)
        DP = [[-1 for _ in range(length_A + 1)] for _ in range(length_B + 1)]
        self.max_length = 0

        def dfs(A, B, i, j):
            if i == -1 or j == -1:
                return 0
            if DP[i][j] >= 0:
                return DP[i][j]

            dfs(A, B, i - 1, j)
            dfs(A, B, i, j - 1)

            if A[i] == B[j]:
                ans = 1
                sub_length = dfs(A, B, i - 1, j - 1)
                self.max_length = max(self.max_length, ans + sub_length)
                DP[i][j] = ans + sub_length
                return ans + sub_length
            else:
                DP[i][j] = 0
                return 0

        dfs(A, B, length_A -1 , length_B - 1)
        return self.max_length




