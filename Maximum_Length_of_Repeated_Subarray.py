# Given two integer arrays A and B, return the maximum length of an subarray that appears in both arrays.
# Example 1:
# Input:
# A: [1,2,3,2,1]
# B: [3,2,1,4,7]
# Output: 3
# Explanation:
# The repeated subarray with maximum length is [3, 2, 1].
# Note:
# 1 <= len(A), len(B) <= 1000
# 0 <= A[i], B[i] < 100


# DP formula:
# F(i,j) = 1 + F(i-1, j-1) if A[i] == B[j]
# F(i,j) = 0 if A[i] != B[i]
# Iterative.
# 54 / 54 test cases passed.
# Status: Accepted
# Runtime: 6066 ms
class Solution(object):
    def findLength(self, A, B):
        """
        :type A: List[int]
        :type B: List[int]
        :rtype: int
        """
        length_A = len(A) + 1
        length_B = len(B) + 1
        max_length = 0
        DP = [[0 for _ in range(length_A)] for _ in range(length_B)]
        for i in range(1, length_A):
            for j in range(1, length_B):
                if A[i - 1] == B[j - 1]:
                    DP[i][j] = DP[i - 1][j - 1] + 1
                    max_length = max(max_length, DP[i][j])

        return max_length

